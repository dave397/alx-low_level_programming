#include "3-calc.h"

/**
 * op_add - add two numbers
 * @a: number one
 * @b: number two
 * Return: addition of one and two
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: number one
 * @b: number two
 * Return: subtraction of two and one
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: number one
 * @b: number two
 * Return: multiplication of one and two
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: number one
 * @b: number two
 * Return: division of one by two
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two numbers
 * @a: number one
 * @b: number two
 * Return: modulus of one and two
 */

int op_mod(int a, int b)
{
	return (a % b);
}
