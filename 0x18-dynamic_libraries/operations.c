
/**
 * add - add two numbers
 * @a: integer 1
 * @b: integer 2
 * Return: addition of a and b
*/

int add(int a, int b)
{
    return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: integer 1
 * @b: integer 2
 * Return: subtraction of a and b
*/

int sub(int a, int b)
{
    return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: integer 1
 * @b: integer 2
 * Return: multiplication of a and b
*/
int mul(int a, int b)
{
    return (a * b);
}

/**
 * div - add to numbers
 * @a: integer 1
 * @b: integer 2
 * Return: division of a and b
*/
int div(int a, int b)
{
    return (a / b);
}

/**
 * mod - modulus of two numbers
 * @a: integer 1
 * @b: integer 2
 * Return: modulus of a and b
*/
int mod(int a, int b)
{
    return (a % b);
}